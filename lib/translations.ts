// Price conversion rates (approximate)
export const EXCHANGE_RATE_THB_TO_MMK = 60 // 1 THB ≈ 60 MMK

export function convertPrice(thbPrice: string | number, locale: Locale): string {
  // Handle both string and number inputs
  const numericPrice = typeof thbPrice === "string" ? Number.parseInt(thbPrice.replace(/[^\d]/g, ""), 10) : thbPrice

  if (locale === "mm") {
    const mmkPrice = numericPrice * EXCHANGE_RATE_THB_TO_MMK
    // Format with Myanmar numerals or standard
    return `${mmkPrice.toLocaleString()} ကျပ်`
  }

  return `฿${numericPrice.toLocaleString()}`
}
